package Vansh.Loops;
import java.util.Scanner;
public class print_numbers_with_for {
    public static void main(String[] args) {
    try (Scanner sc = new Scanner(System.in)) {
        int n =sc.nextInt();
        int counter;
        for(counter=1;counter<=n;counter++){
            System.out.println("Hi Captain");
        }
    }
    } }
